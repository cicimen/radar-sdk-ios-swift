import Foundation

/// The levels for debug logs.
public enum RadarLogLevel : Int {
    /// None
    case none = 0
    /// Error
    case error = 1
    /// Warning
    case warning = 2
    /// Info
    case info = 3
    /// Debug
    case debug = 4
}
